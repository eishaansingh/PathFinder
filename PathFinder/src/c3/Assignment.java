    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
            JFileChooser j = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory()); 
  
            // resctrict the user to selec files of all types 
            j.setAcceptAllFileFilterUsed(false); 
  
            // set a title for the dialog 
            j.setDialogTitle("Select a .txt file"); 
  
            // only allow files of .txt extension 
            FileNameExtensionFilter restrict = new FileNameExtensionFilter("Only .txt files", "txt"); 
            j.addChoosableFileFilter(restrict); 
  
            // invoke the showsOpenDialog function to show the save dialog 
            int r = j.showOpenDialog(null); 
  
            // if the user selects a file 
            if (r == JFileChooser.APPROVE_OPTION) { 
                // set the label to the path of the selected file 
//                l.setText(j.getSelectedFile().getAbsolutePath()); 
            } 
            // if the user cancelled the operation 
//            else
//                l.setText("the user cancelled the operation");    
            String path = j.getSelectedFile().getAbsolutePath();
            File file = new File(path); 
            try {             
                Scanner sc = new Scanner(file);
                String str = sc.nextLine();                
                JOptionPane.showMessageDialog(Assignment.this, "Uploaded!!! :D");
                int i = Integer.parseInt(str);
                for(int t =0; t < i; t++) {
                    String testLine = sc.nextLine();
                    InputVertex ipt = new InputVertex(testLine);
                    Vertex vert = ipt.addVertex();
                    vertices.add(vert);
           
                }
                str = sc.nextLine();
                int ii = Integer.parseInt(str);
                for(int t = 0; t < ii; t++) {
                    String testLine = sc.nextLine();
                    InputEdge ipt = new InputEdge(testLine);
                    Edge e = ipt.addEdge(vertices);
                    edges.add(e);
                                      
                }
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Assignment.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton9ActionPerformed

