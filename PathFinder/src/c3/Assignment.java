class VertexComparator implements Comparator {
    
    @Override
    public int compare(Object object1, Object object2) {
        Vertex v1 = (Vertex)object1;
        Vertex v2 = (Vertex)object2;
        
        String from1 = v1.getName();
        String from2 = v2.getName();
        
        
        if( from1.compareTo(from2) > 0 ) {
            return 1;
        }
        else if( from1.compareTo(from2) == 0 ) {
            return 0;
        }
        else 
            return -1;
            
    }
}


class EdgeComparator implements Comparator {
    
    @Override
    public int compare(Object object1, Object object2) {
        Edge e1 = (Edge)object1;
        Edge e2 = (Edge)object2;
        
        String from1 = e1.getFrom();
        String from2 = e2.getFrom();
        
        
        if( from1.compareTo(from2) > 0 ) {
            return 1;
        }
        else if( from1.compareTo(from2) == 0 ) {
            return 0;
        }
        else 
            return -1;
            
    }
}

